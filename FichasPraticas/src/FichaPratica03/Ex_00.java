package FichaPratica03;

public class Ex_00 {
    public static void main(String[] args) {


        //Este programa escreve os numeros inteiros de 1 a 10.

        int numero;
        numero = 1;

        //Condição while/enquanto a ação é verdadeira, são executadas as instruções.
        while (numero <=10) {
            System.out.println(numero);
            numero = numero + 1;
        }
    }
}
